function applyLanguage(lang) {
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const textKey = element.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][textKey]) {
      element.textContent = translations[lang][textKey];
    }
  });
  document.querySelectorAll('[data-i18n-link]').forEach(element => {
    const linkKey = element.getAttribute('data-i18n-link');
    if (translations[lang] && translations[lang][linkKey]) {
      element.textContent = translations[lang][linkKey];
    }
  });
  
  if (lang === 'ar') {
    document.documentElement.dir = 'rtl';
    document.documentElement.lang = 'ar';
    document.body.classList.add('rtl');
  } else {
    document.documentElement.dir = 'ltr';
    document.documentElement.lang = lang;
    document.body.classList.remove('rtl');
  }
  
  localStorage.setItem('language', lang);
}

document.addEventListener('DOMContentLoaded', function() {
  const languageSwitcher = document.querySelector('.language-switcher');
  
  if (languageSwitcher) {
    const dropdownToggle = languageSwitcher.querySelector('.dropdown-toggle');
    const currentLangDisplay = languageSwitcher.querySelector('.current-lang');
    
    const savedLang = localStorage.getItem('language') || 'en';
    currentLangDisplay.textContent = savedLang.toUpperCase();
    
    dropdownToggle.addEventListener('click', function(e) {
      e.stopPropagation();
      languageSwitcher.classList.toggle('active');
    });
    
    document.addEventListener('click', function() {
      languageSwitcher.classList.remove('active');
    });
    
    languageSwitcher.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', function() {
        const lang = this.dataset.lang;
        currentLangDisplay.textContent = lang.toUpperCase();
        applyLanguage(lang);
        languageSwitcher.classList.remove('active');
      });
    });
    
    applyLanguage(savedLang);
  }
});